function convertFormat($data)
{
    $r = [];
    foreach ($data as $n => $g) {
        $ax = array_shift($g);
        $ay = array_shift($g);
        $w  = array_shift($g);
        $h  = array_shift($g);
        $nx = array_shift($g);
        $r[$n] = [
            $ax + $w / 2,
            $w,
            $ay + $h / 2,
            0,
            $w / 2,
            $h / 2,
            $nx + $w / 2,
        ];
        foreach ($g as $gg) {
            $r[$n][] = $gg;
        }
    }
    return $r;
}
